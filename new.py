# Python code for printing "Hey Nikita"
def greet_person(name):
    """
    Function to greet a person by their name.
    
    Parameters:
    - name (str): The name of the person to greet.
    """
    greeting = "Hey " + name
    print(greeting)

# Calling the function with the name "Nikita"
greet_person("Nikita")